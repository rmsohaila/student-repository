    public static async Task SeedAsync(ApplicationDBContext context, ILogger<ApplicationDBContextSeed> logger)
    {
        if (!context.Nationalities.Any())
        {
            context.Relationships.AddRange(GetPreconfiguredRelationships());
            context.Nationalities.AddRange(GetPreconfiguredNationalities());

            await context.SaveChangesAsync();

            logger.LogInformation("Seed database associated with context {DbContextName}", typeof(ApplicationDBContext).Name);
        }
    }

    private static IList<Relationship> GetPreconfiguredRelationships()
    {
        return new List<Relationship> { 
            new Relationship { Name = "Parent" },
            new Relationship { Name = "Sibling" },
            new Relationship { Name = "Spouse" },
        };
    }
